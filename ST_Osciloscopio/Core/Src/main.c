void TaskFreucuency (void * parameters)
{

	HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_2);
	uint32_t i;
	i = (uint32_t) parameters;
	switch(i)
	{
	case 0:
		HAL_GPIO_WritePin(MUX_SEL0_GPIO_Port, MUX_SEL0_Pin, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(MUX_SEL1_GPIO_Port, MUX_SEL1_Pin, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(MUX_SEL2_GPIO_Port, MUX_SEL2_Pin, GPIO_PIN_RESET);
		while(1)
		{
			if(xSemaphoreTake(xSemaphoreFrecuency0, portMAX_DELAY) == pdTRUE)
			{
				Cuenta++;
			}
			if(xSemaphoreTake(xSemaphoreFrecuency1,0)==pdTRUE)
			{
				Frecuencia = (uint32_t)(1.024 * ((float) Cuenta) -2.1196);
				//Frecuencia = Cuenta;
				Cuenta = 0;
				pData[0] = (uint8_t) (Frecuencia/10000) + '0';
				Frecuencia %=10000;
				pData[1] = (uint8_t) (Frecuencia/1000) + '0';
				Frecuencia %=1000;
				pData[2] = ',';
				pData[3] = (uint8_t) (Frecuencia/100) + '0';
				Frecuencia %=100;
				pData[4] = (uint8_t) (Frecuencia/10) + '0';
				Frecuencia %=10;
				pData[5] = (uint8_t) (Frecuencia) + '0';
				pData[6] = 'H';
				pData[7] = 'z';
				pData[8] = '\r';
				pData[9] = '\n';
				if(pData[0] == '0')
				{
					if(pData[1]== '0')
					{
						if(pData[3]=='0')
						{
							pData[6] = 'H';
							pData[7] = 'z';
							pData[8] = '\r';
							pData[9] = '\n';
							HAL_UART_Transmit(&huart2, &pData[4], 6, HAL_MAX_DELAY);

						} else {
							pData[6] = 'H';
							pData[7] = 'z';
							pData[8] = '\r';
							pData[9] = '\n';
							HAL_UART_Transmit(&huart2, &pData[3], 7, HAL_MAX_DELAY);
						}

					} else {
						pData[5] = 'K';
						pData[6] = 'H';
						pData[7] = 'z';
						pData[8] = '\r';
						pData[9] = '\n';
						HAL_UART_Transmit(&huart2, &pData[1], 10, HAL_MAX_DELAY);
									}
				}  else {
					pData[5] = 'K';
					pData[6] = 'H';
					pData[7] = 'z';
					pData[8] = '\r';
					pData[9] = '\n';
					HAL_UART_Transmit(&huart2, pData, 10, HAL_MAX_DELAY);
				}
			}


		}
	case 1:
		while(1)
		{
			vTaskDelay(1000/portTICK_RATE_MS);
			xSemaphoreGive(xSemaphoreFrecuency1);
		}
	}
}

